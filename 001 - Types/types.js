// Undefined
var a;
console.log('Here, a is a ' + typeof a);

// Undefined
var a = undefined;
console.log('Here, a is a ' + typeof a);

// Number
a = 10;
console.log('Here, a is a ' + typeof a);

// String
a = 'Learning JS';
console.log('Here, a is a ' + typeof a);

// Boolean
a = true;
console.log('Here, a is a ' + typeof a);

// Object
a = new Object();
console.log('Here, a is a ' + typeof a);

// Object
a = {};
console.log('Here, a is a ' + typeof a);

// Object
a = [];
console.log('Here, a is a ' + typeof a);

// Function Object
a = function() {};
console.log('Here, a is a ' + typeof a);

// Object with null reference
a = null;
console.log('Here, a is a ' + typeof a);

// Symbol
a = Symbol("Hello :)");
console.log('Here, a is a ' + typeof a);

// Number
a = NaN;
console.log('Here, a is a ' + typeof a);

// Number
a = Infinity;
console.log('Here, a is a ' + typeof a);

